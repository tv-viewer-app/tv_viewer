def main():
    parser = argparse.ArgumentParser(
        description="TV Viewer — Supabase Setup",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python scripts/supabase_setup.py                    # Interactive setup\n"
            "  python scripts/supabase_setup.py --test-only        # Test existing .env\n"
            "  python scripts/supabase_setup.py --verify-schema    # Check tables exist\n"
            "  python scripts/supabase_setup.py --url URL --key KEY  # Non-interactive\n"
        ),
    )
    parser.add_argument("--url", help="Supabase project URL")
    parser.add_argument("--key", help="Supabase anon key")
    parser.add_argument(
        "--test-only",
        action="store_true",
        help="Only test connectivity (load from .env)",
    )
    parser.add_argument(
        "--verify-schema",
        action="store_true",
        help="Verify that required tables exist",
    )
    args = parser.parse_args()

    # Determine credentials source
    url = args.url or os.environ.get("SUPABASE_URL", "")
    key = args.key or os.environ.get("SUPABASE_ANON_KEY", "")

    # Try loading from .env if not provided
    if not url or not key:
        env = load_env_file(ENV_FILE)
        url = url or env.get("SUPABASE_URL", "")
        key = key or env.get("SUPABASE_ANON_KEY", "")

    # Test-only mode: just run tests with existing credentials
    if args.test_only:
        if not url or not key:
            print(red("  ERROR: No credentials found."))
            print("  Run 'python scripts/supabase_setup.py' to configure,")
            print("  or set SUPABASE_URL and SUPABASE_ANON_KEY env vars.")
            sys.exit(1)
        success = run_tests(url, key)
        sys.exit(0 if success else 1)

    # Schema verification only
    if args.verify_schema:
        if not url or not key:
            print(red("  ERROR: No credentials found."))
            sys.exit(1)
        print(bold("\n  Verifying schema..."))
        all_ok = True
        for table in REQUIRED_TABLES:
            ok, msg = verify_table_exists(url, key, table)
            status = green("✓") if ok else red("✗")
            print(f"  {status} {msg}")
            if not ok:
                all_ok = False
        sys.exit(0 if all_ok else 1)

    # Interactive setup
    if not url or not key:
        url, key = prompt_credentials()
    else:
        # Validate provided credentials
        ok, result = validate_url(url)
        if not ok:
            print(red(f"  Invalid URL: {result}"))
            sys.exit(1)
        url = result

        ok, result = validate_key(key)
        if not ok:
            print(red(f"  Invalid key: {result}"))
            sys.exit(1)
        key = result

    # Run connectivity tests
    success = run_tests(url, key)

    if not success:
        print()
        print(red("  Setup failed. Please fix the issues above and try again."))
        print()
        print("  Common fixes:")
        print("  1. Run supabase_setup.sql in the Supabase SQL Editor")
        print("  2. Check that the URL and anon key are correct")
        print("  3. Ensure your Supabase project is active (not paused)")
        sys.exit(1)

    # Save to .env file
    print()
    print(bold("  Saving credentials..."))

    env_data = load_env_file(ENV_FILE)  # Preserve any existing entries
    env_data["SUPABASE_URL"] = url
    env_data["SUPABASE_ANON_KEY"] = key
    save_env_file(ENV_FILE, env_data)
    apply_env_to_os(env_data)

    print(f"  {green('✓')} Saved to {ENV_FILE}")
    print(f"  {green('✓')} .env is gitignored (will not be committed)")

    # Print next steps
    print_github_secrets_instructions(url, key)
    print_local_dev_instructions()

    print(bold("=" * 60))
    print(green(bold("  ✓ Supabase setup complete!")))
    print(bold("=" * 60))
    print()


if __name__ == "__main__":
    main()
